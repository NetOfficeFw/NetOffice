		/// <summary>
        /// Creates active sink helper
        /// </summary>
		[EditorBrowsable(EditorBrowsableState.Never), Browsable(false)]
		public virtual void CreateEventBridge()
        {
			if(false == Factory.Settings.EnableEvents)
				return;
	
			if (null != _connectPoint)
				return;
	
            if (null == _activeSinkId)
				_activeSinkId = SinkHelper.GetConnectionPoint(this, ref _connectPoint, NetOffice.VisioApi.Behind.EventContracts.EShape_SinkHelper.Id);


			if(NetOffice.VisioApi.Behind.EventContracts.EShape_SinkHelper.Id.Equals(_activeSinkId, StringComparison.InvariantCultureIgnoreCase))
			{
				_eShape_SinkHelper = new NetOffice.VisioApi.Behind.EventContracts.EShape_SinkHelper(this, _connectPoint);
				return;
			} 
        }

