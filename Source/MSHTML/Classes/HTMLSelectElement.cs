        public void DisposeEventBridge()
        {
			if( null != _hTMLSelectElementEvents_SinkHelper)
			{
				_hTMLSelectElementEvents_SinkHelper.Dispose();
				_hTMLSelectElementEvents_SinkHelper = null;
			}

			_connectPoint = null;
		}
        
