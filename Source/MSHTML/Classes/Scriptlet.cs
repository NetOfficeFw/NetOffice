        [EditorBrowsable(EditorBrowsableState.Never), Browsable(false)]
        public void DisposeEventBridge()
        {
			if( null != _dWebBridgeEvents_SinkHelper)
			{
				_dWebBridgeEvents_SinkHelper.Dispose();
				_dWebBridgeEvents_SinkHelper = null;
